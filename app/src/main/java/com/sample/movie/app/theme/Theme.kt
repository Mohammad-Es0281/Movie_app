var isDarkMode = false

@ChecksSdkIntAtLeast(api = Build.VERSION_CODES.S)
fun supportsDynamicTheming() = Build.VERSION.SDK_INT >= Build.VERSION_CODES.S

@Composable
fun MovieTheme(
    darkMode: DarkMode = DarkMode.System,
    appTheme: AppTheme = AppTheme.Default,
    language: String = LocaleProvider.getLanguage(LocalContext.current),
    content: @Composable () -> Unit
) {
    val isDarkTheme = when (darkMode) {
        DarkMode.On -> true
        DarkMode.Off -> false
        DarkMode.System -> isSystemInDarkTheme()
    }
    val context = LocalContext.current

    isDarkMode = isDarkTheme
    val isDynamicThemingEnabled = appTheme == AppTheme.System
    val colorScheme = when {
        isDynamicThemingEnabled && supportsDynamicTheming() -> {
            if (isDarkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        isDarkTheme -> darkScheme

        else -> lightScheme
    }
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.primary.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = isDarkTheme
        }
    }

    val defaultBackgroundTheme = BackgroundTheme(
        color = colorScheme.surface,
        tonalElevation = 2.dp,
    )
    val backgroundTheme = when {
        else -> defaultBackgroundTheme
    }

    val layoutDirection = when (language) {
        AppLanguage.English.code -> LayoutDirection.Ltr
        AppLanguage.Persian.code -> LayoutDirection.Rtl
        else -> LayoutDirection.Rtl
    }

    CompositionLocalProvider(
        LocalBackgroundTheme provides backgroundTheme,
        LocalLayoutDirection provides layoutDirection
    ) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = typography,
            content = content,
        )
    }
}